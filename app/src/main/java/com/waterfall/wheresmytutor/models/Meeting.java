package com.waterfall.wheresmytutor.models;

public class Meeting {
    private String reason, message, date, timeSlot, status, studentName, tutorName, studentId, tutorId, meetingId;
    private double latitude, longitude;

    public Meeting(String reason, String message, String date, String timeSlot, String studentName, String tutorName, String studentId, String tutorId, String meetingId) {
        this.reason = reason;
        this.message = message;
        this.date = date;
        this.timeSlot = timeSlot;
        this.studentName = studentName;
        this.tutorName = tutorName;
        this.studentId = studentId;
        this.tutorId = tutorId;
        this.meetingId = meetingId;
        latitude = -1;
        longitude = -1;
        status = "Pending";
    }

    public Meeting(){
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTimeSlot() {
        return timeSlot;
    }

    public void setTimeSlot(String timeSlot) {
        this.timeSlot = timeSlot;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getStudentId() {
        return studentId;
    }

    public void setStudentId(String studentId) {
        this.studentId = studentId;
    }

    public String getTutorId() {
        return tutorId;
    }

    public void setTutorId(String tutorId) {
        this.tutorId = tutorId;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public String getTutorName() {
        return tutorName;
    }

    public void setTutorName(String tutorName) {
        this.tutorName = tutorName;
    }

    public String getMeetingId() {
        return meetingId;
    }

    public void setMeetingId(String meetingId) {
        this.meetingId = meetingId;
    }
}
